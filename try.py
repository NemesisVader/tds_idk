def compute_slope(raw_text: str) -> float:
    # Extract only integer lines
    lines = raw_text.splitlines()
    views = [int(line) for line in lines if re.fullmatch(r"-?\d+", line.strip())]

    # Prepare data for regression
    X = np.arange(len(views)).reshape(-1, 1)
    y = np.array(views)

    # Fit and return slope
    model = LinearRegression().fit(X, y)
    return round(model.coef_[0], 3)

